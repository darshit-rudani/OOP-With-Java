
public class Primitive {

	public static void main(String[] args) {
		
		// int datatype
		
		    int num;
		    num = 38;
		    
		    System.out.println(num);
		    
		// short int datatype
		
			short num1;
		    num1 = 12345;
		    System.out.println(num1);
		    
		// long int datatype
		    
		    long num2;
		    num2 = 123456789;
		    
		    System.out.println(num2);
		    
		// Float datatype
		    
		    float num3;
		    num3 = 12.45f;
		    
		    System.out.println(num3);
		    
        // double int datatype
		    
		    double num4;
		    num4 = 12345678944.2d;
		    
		    System.out.println(num4);
	    
		// charctor datatype
	    
		    char ch;
		    ch = 'D';
		    
		    System.out.println(ch);	    
		  
	   // Boolean datatype
	
		    boolean b=false;
		    
		    System.out.print(b);
		    
		    
	}
}
