
public class Inc_Dec_operator {

	public static void main(String[] args) {
		
		int num1 = 100;
		int num2 = 200;
		int num3 = 300;
		int num4 = 400;
		
		num1++;
		++num2;
		num3--;
		--num4;
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
	}
}
