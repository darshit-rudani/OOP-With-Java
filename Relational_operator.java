
public class Relational_operator {

	public static void main(String[] args) {
		
		int num1 = 100;
		int num2 = 50;
		
		// (==)
		
			if(num1 == num2) {
				
				System.out.println("Num1 is equal to Num2");
			}
			else
			{
				System.out.println("Num1 is not equal to Num2");
			}
			
		// (!=)
			
			if(num1 != num2) {
				
				System.out.println("Num1 is not equal to Num2");
			}
			else
			{
				System.out.println("Num1 is equal to Num2");
			}
			
		// ( > )
			
			if(num1 > num2) {
				
				System.out.println("Num1 is Greater then Num2");
			}
			else
			{
				System.out.println("Num1 is not Greate then Num2");
			}
			
		// ( < )
			
			if(num1 == num2) {
				
				System.out.println("Num1 is Less then Num2");
			}
			else
			{
				System.out.println("Num1 is not Less then Num2");
			}
			
		// ( >= )
			
			if(num1 == num2) {
				
				System.out.println("Num1 is Greater then or equal to Num2");
			}
			else
			{
				System.out.println("Num1 is Greater then or not equal to Num2");
			}
		
		// ( <== )
		
			if(num1 == num2) {
				
				System.out.println("Num1 is Less then or equal to Num2");
			}
			else
			{
				System.out.println("Num1 is Less then or not equal to Num2");
			}
	}
}
