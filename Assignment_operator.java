
public class Assignment_operator {

	public static void main(String[] args) {
		
		int num1 = 50;
		int num2 = 10;
		
		num1 += num2;
		System.out.println(num1);
		
		num1 -= num2;
		System.out.println(num1);
		
		num1 *= num2;
		System.out.println(num1);
		
		num1 /= num2;
		System.out.println(num1);
		
		num1 %= num2;
		System.out.println(num1);
		
	}
}
